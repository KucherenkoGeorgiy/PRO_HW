package com.hillel.kucherenko.hw7.task1;


public class Car {
    String brand;
    String model;
    String yearOfIssue;
    String color;
    String price;
    String vinCode;

    Car(String brand, String model, String yearOfIssue, String color, String price, String vinCode) {
        this.brand = brand;
        this.model = model;
        this.yearOfIssue = yearOfIssue;
        this.color = color;
        this.price = price;
        this.vinCode = vinCode;
    }
}
